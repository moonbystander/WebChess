package com.example.javawebclass.Pojo;

public class Step {
    private int oldx;
    private int oldy;
    private int newx;
    private int newy;

    public Step() {
    }

    public int getOldx() {
        return oldx;
    }

    public void setOldx(int oldx) {
        this.oldx = oldx;
    }

    public int getOldy() {
        return oldy;
    }

    public void setOldy(int oldy) {
        this.oldy = oldy;
    }

    public int getNewx() {
        return newx;
    }

    public void setNewx(int newx) {
        this.newx = newx;
    }

    public int getNewy() {
        return newy;
    }

    public void setNewy(int newy) {
        this.newy = newy;
    }
}
